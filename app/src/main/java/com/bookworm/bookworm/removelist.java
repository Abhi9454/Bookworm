package com.bookworm.bookworm;

public class removelist {

    public String itemid;
    public String imagename;
    public String names;
    public String removeprice;
    public String course;
    public String semester;
    public String subject;

    public removelist(String itemid, String imagename, String names, String removeprice, String course, String semester, String subject) {
        this.itemid = itemid;
        this.imagename = imagename;
        this.names = names;
        this.removeprice = removeprice;
        this.course = course;
        this.semester = semester;
        this.subject = subject;
    }
}
